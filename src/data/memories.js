const memoryRoot = "/memories";

const createMemoryItems = (folder, files) =>
  files.map((file) => {
    if (typeof file === "string") {
      return {
        src: `${memoryRoot}/${folder}/${file}`,
        alt: `${folder} memory`,
      };
    }

    return {
      ...file,
      src: file.src || `${memoryRoot}/${folder}/${file.file}`,
    };
  });

